window.getDiyActivityUrl = function (activity) {
  if (activity.href) return activity.href;
  return 'toysrus-diy-article.html?slug=' + encodeURIComponent(activity.slug);
};

window.findDiyActivityBySlug = function (slug) {
  return window.DIY_ACTIVITIES.find(function (activity) {
    return activity.slug === slug;
  }) || null;
};
